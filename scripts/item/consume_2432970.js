var status = -1;

function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1 || mode == 0) {
        cm.dispose();
        return;
    }
    if (mode == 1) {
        status++;
    }

    if (status == 0) {
        cm.getPlayer().gainHonor(10000);
        cm.getPlayer().message("명성치가 10,000 만큼 상승 하였습니다.");
        cm.gainItem(2432970, -1);
        cm.dispose();
    }
}

