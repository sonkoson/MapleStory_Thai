function needscheck() {
    nohave = 0;
    acane_check = false;
    if (sel != 9 && sel != 19 && sel != 23 && sel_array[eqp_sel][0] != 1162410 && sel_array[eqp_sel][0] != 1662900 && sel_array[eqp_sel][0] != 1662901) {
        if (!cm.haveItem(sel_array[eqp_sel][1])) {
            nohave++;
        }
    } else if (sel_array[eqp_sel][0] == 1662900) {
        if (!cm.haveItem(sel_array[eqp_sel][1][1]) || !cm.haveItem(sel_array[eqp_sel][1][2]) || !cm.haveItem(sel_array[eqp_sel][1][3]) || !cm.haveItem(sel_array[eqp_sel][1][4]) || !cm.haveItem(sel_array[eqp_sel][1][5])) {
            nohave++;
        }
    } else if (sel_array[eqp_sel][0] == 1662901) {
        if (!cm.haveItem(sel_array[eqp_sel][1][1]) || !cm.haveItem(sel_array[eqp_sel][1][2]) || !cm.haveItem(sel_array[eqp_sel][1][3]) || !cm.haveItem(sel_array[eqp_sel][1][4]) || !cm.haveItem(sel_array[eqp_sel][1][5]) || !cm.haveItem(sel_array[eqp_sel][1][6])) {
            nohave++;
        }
    } else {
        for (i = 1; i < sel_array[eqp_sel][1].length; i++) {
            if (cm.haveItem(sel_array[eqp_sel][1][i])) {
                sel_array[eqp_sel][1] = sel_array[eqp_sel][1][i];
                //cm.getPlayer().dropMessage(-8, "? : " + cm.getItemName(sel_array[eqp_sel][1]));
                acane_check = true;
                break;
            }
        }
    }
    if (!acane_check && (sel == 9 || sel == 19 || sel == 23 || sel_array[eqp_sel][0] == 1162410)) {
        nohave++;
    }
    for (i = 0; i < sel_array[eqp_sel][2].length; i++) {
        if (cm.itemQuantity(sel_array[eqp_sel][2][i][0]) < sel_array[eqp_sel][2][i][1]) {
            nohave++;
            //cm.getPlayer().dropMessage(-8, "? : " + sel_array[eqp_sel][2][i][0]);
        }
    }
    //cm.getPlayer().dropMessage(-8, "nohave : " + nohave);
    if (nohave > 0) {
        return false;
    } else {
        return true;
    }
}

